    public boolean estacerrado(Object obj) {
        JInternalFrame[] activos = menuPrincipal.panel.getAllFrames();
        boolean cerrado = true;
        int i = 0;
        while (i < activos.length && cerrado) {
            if (activos[i] == obj) {
                cerrado = false;
            }
            i++;
        }
        return cerrado;
    }

